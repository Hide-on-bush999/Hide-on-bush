a = [1,4,7,2,5,8,3,6,9]
print(a)
a[1] = 44#将第二个元素替换为44
a[2] = 77#将第三个元素替换为77
print(a)
a[1:3] = [4,7]#将第二，三个元素替换为4和7
print(a)
a.sort()#将列表里的元素由小到大进行排序
print(a)
a.reverse()#将列表里的元素由大到小进行排序
print(a)
b = [1,2,2,2,3,5,9,10,10]
print(b.count(2))#查询列表中有几个2
print(a.index(2))#查询列表中第一个2出现的位置
print(b.index(2,2,6))#查询列表中从第二个元素到第六个元素中2的个数
c = a.copy()#将a列表复制到c
print(c)
d = b[:]#将b列表复制到d
print(d)
