package assess.Three;

public class one {
    public static void main(String[] args) {
        char s1 = 'a';
        String s2 = "bcd";
        int num = 1;
        System.out.println(num);
        System.out.println(s1 + s2 + num);
        System.out.println(s1 + num + s2);
        System.out.println(s2 + num + s1);
        System.out.println(s1 + (num + s2));
        System.out.println(s2 + (num + s1));
    }
}
