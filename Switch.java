package choose;

public class Switch {
    public static void main(String[] args) {
        int i = 2;
        switch (i) {
            case 1:
                System.out.println("一");
                break;
            case 2:
                System.out.println("二");
                break;
            case 3:
                System.out.println("三");
                break;
            case 4:
                System.out.println("四");
                break;
            default:
                System.out.println("无");
                break;
        }
    }
}
